package src.Oops_concept;

public class string_immutable {
    public static void main(String[] args) {


        String s1 = "Hello";
        String s2 = s1.concat("Wolrd");

        System.out.println(s1);
        System.out.println(s2);

        s1 =  "Maruf";
        System.out.println(s1);

        String s3 = "Hello";

//        In short original string will unchanged -> String Immutable

//        String Immutable -> To increase the efficiency, security and thread-safe









    }
}
