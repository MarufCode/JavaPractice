package src.basics_03;

public class incrementOperator {
    public static void main(String[] args) {

        // Increment Operator

        int a = 10;

        a = a+1;
        System.out.println(a);

        a+=1;

        a++;  // POST Increment
        ++a;  // Pre Increment

        int x = 10;
        System.out.println(++x);  // Pre Increment

        int y = 20;
        System.out.println(y++);  // POST Increment
        System.out.println(y);

































    }
}
