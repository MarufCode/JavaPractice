package src.basics_03;

public class ternaryOperator {

    public static void main(String[] args) {

        // Ternary Operator

        // int condition ? if this is true print this || if false do this


//        int a = true ? 10:20;
//        System.out.println(a);

        int a1 = 30>40 ? 10:40;
        System.out.println(a1);


        String s = 10>20 ? "10" : "TWENTY";
        System.out.println(s);



        // MAX of two numbers

//
        int a = 10;
        int b = 30;

        int max = b>a ? b:a;
        System.out.println(max);


        int x = 10;
        int y = 20;

        int max1 = x>y ? x:y;
        System.out.println(max1);

















    }
}
