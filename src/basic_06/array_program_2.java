package src.basic_06;

public class array_program_2 {
    public static void main(String[] args) {

        int marks [] = new int[5];

        marks[0] = 45;
        marks[1] = 68;
        marks[2] = 86;
        marks[3] = 478;
        marks[4] = 85;

        int i = 0;

        while (i<marks.length){
            System.out.println(marks[i]);
            i++;
        }
        System.out.println("-- End of Program -- ");























    }

}
